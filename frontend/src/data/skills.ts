// Helper functions
export const getSkillsByCategory = (category: string): Skill[] => {
  return skills.filter(skill => skill.category === category);
};

export const getSkillsByProficiency = (minProficiency: number): Skill[] => {
  return skills.filter(skill => (skill.proficiency ?? 0) >= minProficiency);
};

export const getSkillsByProject = (projectId: string): Skill[] => {
  return skills.filter(skill => (skill.projects ?? []).includes(projectId));
};

export const getTopSkills = (limit = 10): Skill[] => {
  return skills
    .sort((a, b) => (b.proficiency ?? 0) - (a.proficiency ?? 0))
    .slice(0, limit);
};

export const getSkillsByYearsOfExperience = (minYears: number): Skill[] => {
  return skills.filter(skill => (skill.yearsOfExperience ?? 0) >= minYears);
};

export const getTotalSkills = (): number => {
  return skills.length;
};

export const getAverageProficiency = (): number => {
  const totalProficiency = skills.reduce(
    (sum, skill) => sum + (skill.proficiency ?? 0),
    0
  );
  return skills.length > 0 ? Math.round(totalProficiency / skills.length) : 0;
};

export const getSkillCategories = (): string[] => {
  return [...new Set(skills.map(skill => skill.category))];
};
