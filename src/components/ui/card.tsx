import * as React from "react"
import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: 'default' | 'glass' | 'gradient' | 'elevated';
  }
>(({ className, variant = 'default', ...props }, ref) => {
  const variantClasses = {
    default: "rounded-xl border bg-card text-card-foreground shadow-modern hover:shadow-modern-lg transition-all duration-200",
    glass: "rounded-xl glass text-card-foreground hover:bg-white/20 transition-all duration-200",
    gradient: "rounded-xl bg-gradient-surface text-card-foreground shadow-modern hover:shadow-modern-lg transition-all duration-200",
    elevated: "rounded-xl border bg-card text-card-foreground shadow-modern-xl hover:shadow-2xl transition-all duration-200 hover:-translate-y-1",
  };

  return (
    <div
      ref={ref}
      className={cn(variantClasses[variant], className)}
      {...props}
    />
  );
})
Card.displayName = "Card"

const CardHeader = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn("flex flex-col space-y-2 p-6 pb-4", className)}
    {...props}
  />
))
CardHeader.displayName = "CardHeader"

